package xlang.io

object ConsoleRt {
    @JvmStatic
    fun print(value: Boolean) = kotlin.io.print(value)

    @JvmStatic
    fun print(value: Char) = kotlin.io.print(value)

    @JvmStatic
    fun print(value: Byte) = kotlin.io.print(value)

    @JvmStatic
    fun print(value: Short) = kotlin.io.print(value)

    @JvmStatic
    fun print(value: Int) = kotlin.io.print(value)

    @JvmStatic
    fun print(value: Long) = kotlin.io.print(value)

    @JvmStatic
    fun print(value: Float) = kotlin.io.print(value)

    @JvmStatic
    fun print(value: Double) = kotlin.io.print(value)

    @JvmStatic
    fun println(value: Boolean) = kotlin.io.println(value)

    @JvmStatic
    fun println(value: Char) = kotlin.io.println(value)

    @JvmStatic
    fun println(value: Byte) = kotlin.io.println(value)

    @JvmStatic
    fun println(value: Short) = kotlin.io.println(value)

    @JvmStatic
    fun println(value: Int) = kotlin.io.println(value)

    @JvmStatic
    fun println(value: Long) = kotlin.io.println(value)

    @JvmStatic
    fun println(value: Float) = kotlin.io.println(value)

    @JvmStatic
    fun println(value: Double) = kotlin.io.println(value)
}
